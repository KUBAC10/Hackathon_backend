// assign $Lock field to each label to process automatic translation for labels, messages, errors
// labels
const contentLabels = {};
Object.keys(labels).forEach((key) => {
  contentLabels[key] = labels[key];
  contentLabels[`${key}$Lock`] = {
    type: Boolean,
    default: false,
    label: `${labels[key].label} - Lock translation`
  };
});

// messages
const contentMessages = {};
Object.keys(messages).forEach((model) => {
  contentMessages[model] = {};
  Object.keys(messages[model]).forEach((key) => {
    contentMessages[model][key] = messages[model][key];
    contentMessages[model][`${key}$Lock`] = {
      type: Boolean,
      default: false,
      label: `${messages[model][key].label} - Lock translation`
    };
  });
});

// errors
const contentErrors = {};
Object.keys(errors).forEach((model) => {
  contentErrors[model] = {};
  Object.keys(errors[model]).forEach((key) => {
    contentErrors[model][key] = errors[model][key];
    contentErrors[model][`${key}$Lock`] = {
      type: Boolean,
      default: false,
      label: `${errors[model][key].label} - Lock translation`
    };
  });
});

export default { contentLabels, contentMessages, contentErrors };
